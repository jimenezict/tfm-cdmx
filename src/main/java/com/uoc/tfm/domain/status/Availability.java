package com.uoc.tfm.domain.status;

public class Availability {

    private int bikes;
    private int slots;

    public Availability() {
    }

    public int getBikes() {
        return bikes;
    }

    public void setBikes(int bikes) {
        this.bikes = bikes;
    }

    public int getSlots() {
        return slots;
    }

    public void setSlots(int slots) {
        this.slots = slots;
    }

}
