package com.uoc.tfm.domain.location;

public class StationLocation {

    private double lat;
    private double lon;

    public StationLocation() {

    }

    public double getLat() {
        return lat;
    }

    public void setLat(double lat) {
        this.lat = lat;
    }

    public double getLon() {
        return lon;
    }

    public void setLon(double lon) {
        this.lon = lon;
    }
}